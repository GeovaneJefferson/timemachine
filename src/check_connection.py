from setup import *

def is_connected(iniHDName):
    ################################################################################
    # External availability
    ################################################################################
    try:
        ################################################################################
        # /media
        ################################################################################
        os.listdir(f"{media}/{userName}/{iniHDName}")  
        return True

    except FileNotFoundError:
        ################################################################################
        # /run/media
        ################################################################################
        try:
            os.listdir(f"{run}/{userName}/{iniHDName}") 
            return True

        except FileNotFoundError:
            pass
    
        return False